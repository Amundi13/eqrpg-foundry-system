function spellItem(name, classId, system) {
  return {
    name,
    type: "spell",
    img: "",
    system: {
      description: system.description ?? "",
      spellLevel: 1,
      manaCost: 1,
      castingTime: "1 action",
      range: "Close (25 ft. + 5 ft./2 levels)",
      duration: "Instantaneous",
      effect: "",
      damageFormula: "",
      healFormula: "",
      school: "",
      classes: [classId],
      classLevels: [`${classId}:1`],
      spellLine: name,
      recastTime: 0,
      savingThrow: "",
      saveEffect: "",
      saveDC: "",
      deliveryType: "utility",
      attackMode: "",
      attackBonus: 0,
      ...system,
      classes: [classId],
      classLevels: [`${classId}:1`],
    },
  };
}

